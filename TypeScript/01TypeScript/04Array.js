/**An array is a special variable, which can hold more than one value at a time. */
var arr = [4, 5, 6];
for (var i = 0; i < arr.length; i++) {
    console.log(arr[i]);
}
console.log("\n");
/**generic array */
var arrList = [9, 8, 7];
arrList.forEach(function (element) {
    console.log(element);
});
